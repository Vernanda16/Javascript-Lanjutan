//Math.log()

/*
Method Math.log() berfungsi untuk menghitung nilai logaritma natural, yaitu nilai dari log e x. 
Fungsi ini membutuhkan 1 buah argumen angka.
*/

function logaritma(){
    console.log(Math.log(1)) //hasilnya akan 0 karena logaritma dari 1 adalah 0
    console.log(Math.log(6));
    console.log(Math.log2(256));    //hasil dari 2 log 256 adalah 8
    console.log(Math.log10(100));
}

logaritma();