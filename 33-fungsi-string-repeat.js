//fungsi repeat() berguna untuk mengulang kalimat

function cthRepeat(){
    var kalimat = "bismillah ";

    console.log(kalimat.repeat(5)); //ini artinya kata bismillah akan di cetak sebanyak 5 kali
    console.log("semoga sukses -". repeat(4));  //ini artinya kalimat semoga sukses akan dicetak sebanyak 4 kali
}


cthRepeat();

//output
/*
bismillah bismillah bismillah bismillah bismillah 
semoga sukses -semoga sukses -semoga sukses -semoga sukses -
*/